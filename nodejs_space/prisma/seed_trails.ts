// ─── FUNÇÃO PRINCIPAL DE SEED ─────────────────────────────────

async function main() {
  // Pula se o seed já foi executado com sucesso anteriormente
  const existingCount = await prisma.questions.count();
  if (existingCount >= 100) {
    console.log(`✅ Seed já executado (${existingCount} questões no banco). Pulando.`);
    return;
  }

  console.log('🌱 Iniciando seed de 100 questões em 10 trilhas...\n');

  for (const trail of trails) {
    console.log(`📚 Criando trilha: ${trail.icon} ${trail.name}`);

    const createdTrail = await prisma.trails.upsert({
      where: { slug: trail.slug },
      update: {
        name: trail.name,
        icon: trail.icon,
        description: trail.description,
        profession: trail.profession,
        order: trail.order,
        color: trail.color,
      },
      create: {
        slug: trail.slug,
        name: trail.name,
        icon: trail.icon,
        description: trail.description,
        profession: trail.profession,
        order: trail.order,
        color: trail.color,
      },
    });

    for (const q of trail.questions) {
      const questionData = {
        trailId: createdTrail.id,
        order: q.order,
        type: q.type as QuestionType,
        title: q.title,
        description: q.description,
        hint: q.hint ?? null,
        xpReward: q.xpReward,
        options: q.options ? JSON.stringify(q.options) : null,
        correctOption: q.correctOption ?? null,
        spreadsheetContext: q.spreadsheetContext ?? null,
        expectedValue: q.expectedValue ?? null,
        targetCell: q.targetCell ?? null,
        correctOrder: q.correctOrder ? JSON.stringify(q.correctOrder) : null,
        explanation: q.explanation,
      };
      await prisma.questions.upsert({
        where: {
          trailId_order: { trailId: createdTrail.id, order: q.order },
        },
        update: questionData,
        create: questionData,
      });
      process.stdout.write('  ✓');
    }

    console.log(`\n  ✅ ${trail.questions.length} questões inseridas\n`);
  }

  const total = await prisma.questions.count();
  console.log(`\n🎉 Seed concluído! Total de questões: ${total}`);
}

main()
  .catch((e) => {
    console.error('❌ Erro no seed:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
    process.exit(0);
  });
